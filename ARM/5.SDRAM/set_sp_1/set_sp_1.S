#define WTCON    0xE2700000
#define SVC_SP   0xD0037D80

.global _start				
 _start:
	
	//关看门狗,操作看门狗的那个寄存器就是WTCON，那个观看门狗的那个功能就是第五位，0表示关，1表示的是开
	ldr r0, =WTCON
	ldr r1, =0x0
	str r1, [r0]
	
	//这里设置栈,这样就设置好了栈了，之后就可以用c语言来编程了
	ldr sp, =SVC_SP
	
	//设置模式，下面的都是功能代码
	bl led_blink
	
	//注意最后还是要加上一个死循环的呀，防止程序出错的时候跑飞了
	b .
 
 
 
 
 
 
 
 