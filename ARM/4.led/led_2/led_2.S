 *	名字：led_2.S
 *	作用：这里是使得中间那个led亮的作用
 *
 */
 
#define GPJ0CON  0xE0200240			//这里使用了宏定义，和c语言中宏定义是一样的用法
#define GPJ0DAT  0xE0200244			//使用宏定义就避免了重复输入数字，容易出错的问题

.global _start
 _start:						
	ldr r0, =GPJ0CON			
	ldr r1, =0x11111111				//GPJ0CON这个寄存器就不需要改变了，他的作用只是改变那个接口的模式而已
	str r1, [r0]					//GPJ0DAT这个寄存器就是改变输出的是什么东西了，所以就改变这个寄存器就可以了
	
	ldr r0, =0x28					//0x28就是00101000就使得中间那个led输出是低电平
	ldr r1, =GPJ0DAT			
	str r0, [r1]
	
	b .						//这里也是一个死循环来的，比较装逼一点而已。
							//和flag：b flag 的作用是一样的，那个 . 就表示是当前的意思，就和linux的用法是一样的
 

 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 