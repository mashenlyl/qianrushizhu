/*
 *
 *	名字：led_2.S
 *	作用：这里是使得中间那个led亮的作用(使用位操作)
 *
 */
 
#define GPJ0CON  0xE0200240			
#define GPJ0DAT  0xE0200244

.global _start
 _start:						
	ldr r0, =GPJ0CON			
	ldr r1, =0x11111111
	str r1, [r0]					
	
	ldr r0, =(1<<3) | (0<<4) | (1<<5)		//这里就是使用了那个或运算，和左移，它的作用和0x2的作用一样			
	ldr r1, =GPJ0DAT						//在中间加了那个0是为了比较容易看清楚代码的作用
	str r0, [r1]
	
	b .									//和那个b . 的作用是一样的啊						
 
/*
总结：
位操作：
	与（&）或（|）非（!）移位（左移 <<  右移 >>）
	它操作起来比较好，人家看起来就比较清楚了
*/
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 